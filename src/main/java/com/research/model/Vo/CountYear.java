package com.research.model.Vo;

import java.math.BigDecimal;

public class CountYear {
    private Long pnumy;

    private BigDecimal omnumy;

    private Long cnumy;

    private BigDecimal imnumy;

    public Long getPnumy() {
        return pnumy;
    }

    public void setPnumy(Long pnumy) {
        this.pnumy = pnumy;
    }

    public BigDecimal getOmnumy() {
        return omnumy;
    }

    public void setOmnumy(BigDecimal omnumy) {
        this.omnumy = omnumy;
    }

    public Long getCnumy() {
        return cnumy;
    }

    public void setCnumy(Long cnumy) {
        this.cnumy = cnumy;
    }

    public BigDecimal getImnumy() {
        return imnumy;
    }

    public void setImnumy(BigDecimal imnumy) {
        this.imnumy = imnumy;
    }
}